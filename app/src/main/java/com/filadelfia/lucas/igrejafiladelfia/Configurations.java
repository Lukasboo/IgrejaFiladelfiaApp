public class Configurations {

    String id;
    String logo;
    String background;
    String line;
    String appointmentsbook;
    String warning;
    String site;
    String message;
    String facebook;
    String ministry;
    String youth;
    String leadership;
    String contact;

    public Configurations(String id, String logo, String background, String line, String appointmentsbook,
                   String warning, String site, String message, String facebook, String ministry,
                   String youth, String leadership, String contact){
        this.id = id;
        this.logo = logo;
        this.background = background;
        this.line = line;
        this.appointmentsbook = appointmentsbook;
        this.warning = warning;
        this.site = site;
        this.message = message;
        this.facebook = facebook;
        this.ministry = ministry;
        this.youth = youth;
        this.leadership = leadership;
        this.contact = contact;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

    public String getAppointmentsbook() {
        return appointmentsbook;
    }

    public void setAppointmentsbook(String appointmentsbook) {
        this.appointmentsbook = appointmentsbook;
    }

    public String getWarning() {
        return warning;
    }

    public void setWarning(String warning) {
        this.warning = warning;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }

    public String getMinistry() {
        return ministry;
    }

    public void setMinistry(String ministry) {
        this.ministry = ministry;
    }

    public String getYouth() {
        return youth;
    }

    public void setYouth(String youth) {
        this.youth = youth;
    }

    public String getLeadership() {
        return leadership;
    }

    public void setLeadership(String leadership) {
        this.leadership = leadership;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }



}
